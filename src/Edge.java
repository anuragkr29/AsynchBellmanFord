public class Edge {
    int weight;
    int UID;

    public Edge(int weight, int UID) {
        this.weight = weight;
        this.UID = UID;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getUID() {
        return UID;
    }

    public void setUID(int UID) {
        this.UID = UID;
    }
}
